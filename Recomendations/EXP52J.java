public class EXP52J {
    public static void main(String[] args) {
        int number = 10;
        if (number > 5) {// open - braces
            System.out.println("The number is greater than 5.");
        } // close braces
        while (number > 2) {// open - braces
            System.out.println("Take 2");
            number -= 2;
        } // close - braces
    }
}
