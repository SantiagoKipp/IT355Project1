package GroupProject1;

/**
 * The {@code NUM52J} class demonstrates basic arithmetic operations
 * involving different data types in Java.
 * 
 * <p>
 * This class adds an integer and a double, then prints the result
 * to the console.
 */
public class NUM52J {
    /**
     * The entry point of the application.
     * <p>
     * This method initializes two numbers of different types,
     * performs an addition operation, and prints the result.
     *
     * @param args The command-line arguments.
     */
    public static void main(String[] args) {
        int num1 = 3; // An integer value.
        double num2 = 5; // A double value.

        // Add the integer and double values together.
        double addNumbers = num1 + num2;

        // Print result of the addition operation.
        System.out.println(addNumbers);
    }
}
